def l1_untested_commands():
    """L1: CLI commands without test coverage."""
    root = _project_root()
    test_dir = root / "tests"
    if not test_dir.exists():
        return {"value": len(EXPECTED_COMMANDS), "weight": 8, "components": {"tested": [], "untested": EXPECTED_COMMANDS}}

    try:
        test_text = ""
        for f in test_dir.glob("*.py"):
            test_text += f.read_text()
    except Exception:
        return {"value": len(EXPECTED_COMMANDS), "weight": 8, "components": {}}

    tested = []
    untested = []
    for cmd in EXPECTED_COMMANDS:
        # Look for test references to command
        if cmd.replace("-", "_") in test_text or cmd in test_text:
            tested.append(cmd)
        else:
            untested.append(cmd)

    return {
        "value": len(untested),
        "weight": 8,
        "components": {"tested": tested, "untested": untested},
    }


def l2_template_drift():
    """L2: Bundled template skills out of sync with repo skills."""
    root = _project_root()
    repo_skills = root / ".claude" / "skills"
    bundled_skills = root / "src" / "ai_cockpit" / "template" / ".claude" / "skills"

    if not repo_skills.exists() or not bundled_skills.exists():
        return {"value": 1.0, "weight": 7, "components": {"error": "dirs missing"}}

    drifted = []
    synced = []
    for skill_dir in sorted(repo_skills.iterdir()):
        if not skill_dir.is_dir():
            continue
        skill_file = skill_dir / "skill.md"
        bundled_file = bundled_skills / skill_dir.name / "skill.md"

        if not skill_file.exists():
            continue

        repo_sha = _file_sha(skill_file)
        bundled_sha = _file_sha(bundled_file)

        if repo_sha == bundled_sha:
            synced.append(skill_dir.name)
        else:
            drifted.append(skill_dir.name)

    total = len(synced) + len(drifted)
    return {
        "value": len(drifted) / total if total else 0.0,
        "weight": 7,
        "components": {"synced": synced, "drifted": drifted},
    }


