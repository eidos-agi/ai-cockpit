def l4_missing_help():
    """L4: Commands that don't handle --help properly."""
    root = _project_root()
    cli_module = root / "src" / "ai_cockpit" / "cli.py"

    # Commands that should have --help handling
    commands_needing_help = ["can-i-close", "touch-and-go", "new", "config"]

    try:
        text = cli_module.read_text()
    except Exception:
        return {"value": len(commands_needing_help), "weight": 5, "components": {}}

    missing = []
    covered = []
    for cmd in commands_needing_help:
        fn_name = f"cmd_{cmd.replace('-', '_')}"
        # Find the function and check if it handles --help
        if f'"--help" in args' in text or f'"--help"' in text:
            covered.append(cmd)
        else:
            missing.append(cmd)

    # More precise: check each function individually
    missing = []
    covered = []
    for cmd in commands_needing_help:
        fn_name = f"cmd_{cmd.replace('-', '_')}"
        # Find function body and look for --help
        idx = text.find(f"def {fn_name}")
        if idx == -1:
            missing.append(cmd)
            continue
        # Look in next 500 chars for --help handling
        snippet = text[idx:idx + 500]
        if "--help" in snippet:
            covered.append(cmd)
        else:
            missing.append(cmd)

    return {
        "value": len(missing),
        "weight": 5,
        "components": {"covered": covered, "missing": missing},
    }


def l5_stale_pypi():
    """L5: Git version doesn't match published PyPI version."""
    from ai_cockpit import __version__ as local_version

    try:
        r = subprocess.run(
            [sys.executable, "-m", "pip", "index", "versions", "ai-cockpit"],
            capture_output=True, text=True, timeout=10,
        )
        if r.returncode == 0 and "Available versions:" in r.stdout:
            # Parse latest version
            versions_line = [l for l in r.stdout.splitlines() if "Available versions:" in l]
            if versions_line:
                latest = versions_line[0].split(":")[1].strip().split(",")[0].strip()
                match = local_version == latest
                return {
                    "value": 0.0 if match else 1.0,
                    "weight": 4,
                    "components": {"local": local_version, "pypi": latest, "match": match},
                }
    except Exception:
        pass

    return {
        "value": 0.0,  # Can't check = assume ok
        "weight": 4,
        "components": {"local": local_version, "pypi": "unknown", "match": "unknown"},
    }


