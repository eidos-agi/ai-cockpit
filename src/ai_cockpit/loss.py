def compute_all():
    """Compute all missions and losses. Returns the full scorecard."""
    missions = {
        "M1_zero_to_flying": m1_zero_to_flying(),
        "M2_fleet_awareness": m2_fleet_awareness(),
        "M3_safe_close": m3_safe_close(),
        "M4_lifecycle_completeness": m4_lifecycle_completeness(),
        "M5_plugin_discovery": m5_plugin_discovery(),
        "M6_self_documenting": m6_self_documenting(),
    }

    losses = {
        "L1_untested_commands": l1_untested_commands(),
        "L2_template_drift": l2_template_drift(),
        "L3_hardcoded_paths": l3_hardcoded_paths(),
        "L4_missing_help": l4_missing_help(),
        "L5_stale_pypi": l5_stale_pypi(),
        "L6_code_mass": l6_code_mass(),
    }

    # Weighted mission composite
    m_total_weight = sum(m["weight"] for m in missions.values())
    mission_composite = sum(m["score"] * m["weight"] for m in missions.values()) / m_total_weight

    # Weighted loss composite
    l_total_weight = sum(l["weight"] for l in losses.values())
    loss_composite = sum(l["value"] * l["weight"] for l in losses.values()) / l_total_weight

    return {
        "loss_composite": round(loss_composite, 2),
        "mission_composite": round(mission_composite, 2),
        "missions": missions,
        "losses": losses,
        "snapshot_date": datetime.now().strftime("%Y-%m-%d"),
    }


def print_panel():
    """Print human-readable instrument panel."""
    data = compute_all()

    print()
    print("  " + "=" * 56)
    print(f"    AI-COCKPIT INSTRUMENT PANEL  |  {data['snapshot_date']}")
    print("  " + "=" * 56)
    print(f"    LOSS: {data['loss_composite']:.2f}  (lower is better)")
    print(f"    MISSION: {data['mission_composite']:.2f}  (higher is better, max 1.0)")
    print("  " + "=" * 56)
    print()

    # Mission bars
    for name, m in data["missions"].items():
        pct = int(m["score"] * 100)
        bar_filled = int(m["score"] * 20)
        bar = "\033[32m" + "█" * bar_filled + "\033[90m" + "░" * (20 - bar_filled) + "\033[0m"
        label = name.ljust(28)
        weight = f"(w:{m['weight']})"
        print(f"    {bar} {pct:3d}%  {label} {weight}")

    print()

    # Loss details
    print("    \033[1mLOSSES\033[0m")
    for name, l_data in data["losses"].items():
        val = l_data["value"]
        if val == 0:
            indicator = "\033[32m✓\033[0m"
        elif val < 1:
            indicator = "\033[33m!\033[0m"
        else:
            indicator = "\033[31m✗\033[0m"
        label = name.ljust(28)
        detail = ""
        comps = l_data.get("components", {})
        if "untested" in comps and comps["untested"]:
            detail = f"untested: {', '.join(comps['untested'][:3])}"
        elif "drifted" in comps and comps["drifted"]:
            detail = f"drifted: {', '.join(comps['drifted'][:3])}"
        elif "lines" in comps:
            detail = f"{comps['lines']} lines"
        elif "missing" in comps and comps["missing"]:
            detail = f"missing: {', '.join(comps['missing'])}"
        elif "match" in comps:
            detail = f"local={comps.get('local')} pypi={comps.get('pypi')}"

        print(f"    {indicator} {val:5.2f}  {label} {detail}")

    print()
