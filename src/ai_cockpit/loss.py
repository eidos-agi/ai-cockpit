    try:
        lines = len(cli_file.read_text().splitlines())
    except Exception:
        return {"value": 1.0, "weight": 3, "components": {}}

    # Threshold: 1500 lines is healthy, 2000+ is a smell
    threshold = 1500
    excess = max(0, lines - threshold) / threshold

    return {
        "value": round(excess, 2),
        "weight": 3,
        "components": {"lines": lines, "threshold": threshold},
    }


